<h2>Scopul experimentului</h2>

<p>Utilizarea unui întrerupător într-un circuit simplu. Pentru acest experiment aveţi nevoie de un întrerupător simplu, mecanic (întrerupător de veioză sau de perete, de exemplu). Vom realiza aşadar circuitul de mai jos:</p>

<img src="../poze/05066.png" width="274" height="108" alt="circuit electric cu întrerupător"/>

<p>Practic, circuitul va arăta astfel:</p>

<img src="../poze/05067.png" width="350" height="180" alt="circuit electric cu întrerupător"/>

<h2>Desfăşurarea experimentului</h2>

<p>Construiţi circuitul de mai sus format dintr-o baterie, un întrerupător şi un bec, precum în figura de mai sus. Acest circuit este cu atât mai impresionant cu cât firele conductoare sunt mai lungi, întrucât se poate vedea faptul că întrerupătorul poate controla curentul din circuit, indiferent de mărimea circuitului.</p>

<p>Măsuraţi tensiunea la bornele bateriei, la bornele întrerupătorului (între cele două şuruburi). Masuraţi tensiunea şi la bornele becului, în ambele poziţii ale întrerupătorului. Când întrerupătorul este dezactivat (becul este stins), spunem că acesta este deschis. La fel ca în exemplele precedente, orice întrerupere a circuitului, indiferent de locatie, duce la întreruperea curentului prin întreg circuitul. Rezultatul este, evident, stingerea becului.</p>

