<h2>Desfăşurarea experimentului</h2>

<p>Înfăsuraţi pentru început o bandă izolatoare în jurul barei metalice (sau cui, ce aveţi la dispoziţie). Acest lucru va proteja conductorii împotriva abraziunii. Asiguraţi-vă că după finalizarea înfăsurării, va rămân cele două captele libere pentru a putea alimenta electromagnetul. Înfăşuraţi apoi conductorul izolat în jurul barei metalice de câteva sute de ori, pe cât de egal se poate (nu toate înfăşurările în acelaşi loc). Puteţi desigur să suprapuneţi conductorii, întrucât sunt izolaţi prin lăcuire.</p>

<p>Singura regulă pe care trebuie să o respectaţi, este că toate înfăsurările trebuie să fie realizate în aceiaşi direcţie. De exemplu, toate să fie în sensul acelor de ceasornic. După ce aţi făcut câteva sute de înfăşurări în jurul elementului metalic, înfăşuraţi un nou strat de bandă izolatoare peste conductori. Îndepărtăti lacul izolator de pe capetele conductorilor. Conectaţi apoi aceste capete la o baterie.</p>

<p>La trecerea curentului electric prin înfăşurare, va lua naştere un câmp magnetic destul de puternic: câte un pol la fiecare capăt al elementului metalic. Acest fenoment poartă numele de electromagnetism. Puteţi folosi un compas pentru identificarea polilor magnetic Nord şi Sud ai electromagnetului.</p>

<p>După ce electromagnetul a fost conectat la baterie, aduceţi un magnet permanent în apropierea unuia dintre poli. Observaţi ce tip de forţa apare între cei doi, de atracţie sau de respingere? Inversaţi orientarea magnetului permanent. Ce tip de forţă există în acest caz? Încercaţi să folosiţi electromagnetul pentru a atrage diferite obiecte metalice ce le aveţi la îndemână (agrafe, ace de gămălie, etc.), la fel cum aţi folosi un magnet permanent.</p>



