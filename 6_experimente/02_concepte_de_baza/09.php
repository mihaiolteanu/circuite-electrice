<h2>Scopul experimentului</h2>

<p>Aplicarea „regulii mâini stângi” şi realizarea practică a unui electromagnet. Veţi avea nevoie de un conductor lung de electromagnet. Aceşti conductori nu sunt altceva decât conductori de cupru izolaţi prin lăcuire, folosiţi pentru construirea transformatoarelor sau a motoarelor electrice. Puteţi obţine un astfel de conductor dintr-un transformator (stricat).</p>

<p>Veţi avea de asemenea nevoie de un bolţ, cui sau o bară metalică. Atenţie, oţelul inoxidabil nu este magnetic, si prin urmare nu va putea fi folosit pe post de miez electromagnetic!</p>

