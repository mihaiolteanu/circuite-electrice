<h2>Utilizarea detectorului de câmpuri electrice</h2>

<p>Conectaţi una dintre sondele detectorului la un obiect metalic ce se află în contact cu pământul. Aproape orice ţeavă de apă din casă este bună pentru acest lucru. Luaţi cealaltă sondă şi apropiaţi-o de un aparat electrocasnic. Atenţie, nu încercaţi să realizaţi contactul dintre această sondă şi aparatul electrocasnic sau cu orice conductori din interiorul acestuia! Dacă auziţi un bâzâit în căşţi, veţi sţii că în acea zonă se află un câmp electric.</p>

<p>Ţineţi conductorul de detectare în apropierea unei surse puternice de câmp electric şi încercaţi diferite poziţii ale acestuia. Folositi o bucaţică de aluminiu ataşată la capătul conductorului pentru maximizarea capacităţii, şi prin urmare, a abilităţii acestuia de interceptare a câpurilor electrice.</p>

