<h2>Scopul experimentului</h2>

<p>Ne propunem să realizăm o baterie din cartof. Deşi acest experiment foloseşte cartoful pe post de baterie, puteţi folosi o varietate de fructe şi legume pe post de potenţiale baterii. Veţi avea nevoie prin urmare, pe lângă cartof (sau lămâie, de exemplu), de o bucată de zinc sau de metal galvanizat şi un conductor de cupru. Pentru electrodul de zinc, puteţi folos un cui galvanizat. Vom vedea importanţa reacţiilor chimice în funcţionarea bateriilor precum şi modul în care suprafaţa electrozilor influenţează funcţionarea bateriei. Ciurcuitul realizat este următorul:</p>

<img src="../poze/05167.png" width="324" height="205" alt="baterie din cartof"/>

<h2>Realizarea bateriei din cartof</h2>

<p>Introduceţi atât cuiul cât şi conductorul de cupru în cartof. Măsuraţi tensiunea produsă de baterie cu un voltmetru. Asta e tot!</p>

<p>Experimentaţi însă cu diferite metale, diferite adâncimi şi distanţe între electrozi. Cum puteţi obţine cea mai mare tensiune posibilă cu ajutorul cartofului? Încercaţi alte legume şi comparaţi tensiunile de ieşire pentru metale similare pe post de electrozi.</p>

<p>Desigur, alimentarea unei sarcini cu o astfel de „baterie” este foarte dificilă. Nu vă asteptaţi aşadar să alimentaţi un bec, un motor sau orice altceva. Chiar dacă tensiunea de ieşire este suficient de bună, rezistenţa internă a bateriei este destul de mare. Conectând mai mulţi cartofi în configuraţii serie, paralel sau serie-paralel, putem obţine o tensiune şi un curent destul de mare pentru alimentarea unei sarcini mici. Puteţi realiza acest lucru?</p>

