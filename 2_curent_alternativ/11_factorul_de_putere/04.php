<p>Atunci când avem nevoie de corectarea practică a factorului de putere într-un sistem de putere în curent alternativ, probabil că nu vom fi atât de norocoşi încât să cunoaştem inductanţa exactă a sarcinii. Putem folosi un aparat de măsură special, denumit <i>cosfimetru</i> pentru calcularea factorului de putere. Puterea aparentă o putem calcula folosind un voltmetru şi un ampermetru. În cel mai rău caz însă, am putea fi nevoiţi să folosim un osciloscop pentru calcularea diferenţei de fază, în grade, între formele de undă alte tensiunii şi ale curentului; factorul de putere va fi cosinusul acelui unghi.</p>

<p>Dacă avem acces la un wattmetru pentru măsurarea puterii reale, putem compara valoarea citită cu valoarea puterii aparente deduse din produsul tensiunii totale cu a curentului total.</p>


