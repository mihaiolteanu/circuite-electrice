<p>Să luăm câteva exemple de reprezentare a formelor de undă în curent alternativ cu ajutorul vectorilor, unde lungimea vectorului reprezintă amplitudinea undei, asfel:</p>

<p><img src="../poze/02031.png" alt="reprezentarea formelor de undă în curent alternativ cu ajutorul vectorilor, unde lungimea vectorului reprezintă amplitudinea undei"/></p>

<p>Cu cât amplitudinea formei de undă este mai mare, cu atât lungimea vectorului corespunzător va fi mai mare. Unghiul vectorului, pe de altă parte, reprezintă diferenţa de fază (defazajul) dintre unda considerată si o altă formă de undă de referinţă. De obicei, atunci când exprimăm faza unei forme de undă, punctul de referinţă îl reprezintă forma de undă a sursei de alimentare, considerată a fi O<sup>o</sup>. Ţineţi minte că faza este tot timpul o mărime relativă dintre două unde şi nu o proprietate absolută a undelor.</p>

<p><img src="../poze/02032.png" alt="reprezentarea formelor de undă în curent alternativ cu ajutorul vectorilor; faze şi relaţii pentru defazaje de 0, 90, -90 respectiv 180 de grade"/></p>
<p><img src="../poze/02038.png" alt="relaţia grafică dintre defazaj şi unghiul dintre vectori"/></p>

<p>Cu cât defazajul dintre formele de undă considerate este mai mare, cu atât este mai mare unghiuld dintre vectorii corespunzători.</p>

<p class="sumar">Sumar:</p>
<ul>
	<li>Când este folosit pentru descrierea valorilor în curent alternativ, lungimea unui vector reprezintă amplitudinea undei iar unghiul său reprezintă diferenţa de fază (defazajul) undei faţă de unda de referinţă</li>
</ul>
