<p>În ciuda tuturor măsurilor de siguranţă luate, din când în când au loc şi accidente neplăcute. În majoritatea cazurilor acestea sunt urmarea nerespectării procedurilor de siguranţă. Dar indiferent de natura lor, este bine ca cei ce lucrează în domeniul sistemelor electrice să cunoască măsurile de prim ajutor ce trebuiesc acordate victimei electrocutării.</p>

<p>Primul lucru atunci când observăm o persoană „îngheţată pe circuit” este să oprim alimentarea cu energie electrică de la cel mai apropiat întrerupător. Dacă o a doua persoană vine în contact cu victima, există riscul ca valoarea căderii de tensiune asupra ambelor persoane să fie suficient de mare pentru electrocutarea amândoura, ducând la „îngheţarea” ambelor persoane pe circuit, nu doar a victimei iniţiale. Nu faceţi pe eroul! Electronii nu respectă eroismul.</p>

<p>Una dintre problemele acestei proceduri este imposibilitatea depistării la timp a sursei de energie electrică pentru salvarea victimei. Dacă funcţionarea inimii şi respiraţia victimei electrocutate sunt paralizate de curentul electric, durata de supravieţuire este extrem de limitată. Dacă şocul de curent este suficient de mare, carnea şi organele interne ale victimei sunt făcute scrum într-un timp foarte scurt.</p>

<p>Dacă nu se poate localiza întrerupătorul circuitului într-un timp suficient de scurt, se poate încerca dezlipirea victimei de pe circuit prin smulgere sau lovire cu ajutorul unui lemn uscat(coadă de mătură, de ex) sau orice alt material nonmetalic, izolant, ce poate fi găsit în împrejurime. O metodă bună de îndepărtare a victimei este folosirea unui prelungitor electric plasat în jurul corpului victimei şi tragerea acesteia din preajma circuitului. Ţineţi minte că victima va strânge conductorul cu toată forţ,  prin urmare tragerea şi dezlipirea acesteia de pe circuit nu va fi deloc uşoară!</p>

<p>După îndepărtarea victimei de la sursa de energie electrică, cea mai mare problemă din punct de vedere medical este respiraţia şi circulaţia (pulsul). Pentru a preveni deoxigenarea victimei, se vor aplica metodele de resuscitare cardio-pulmonara (RCP), dacă salvatorul le cunoaşte, până la sosirea unui echipaj medical specializat.</p>

<p>Dacă victima este conştientă, aceasta trebuie ţinută pe loc până la sosirea unui echipaj medical specializat la locul accidentului. Există posibilitatea căderii victimei într-un şoc fiziologic - o situaţie în care circulaţia sângelui nu este suficientă, situaţie diferită de cea a şocului electric - prin urmare, temperatura şi confortul victimei trebuie să fie o prioritate. Este posibil ca un şoc electric iniţial insuficient pentru a cauza întreruperea imediată a bătăii inimii, să cauzeze probleme şi să duc la un infarct câteva ore mai târziu; victima va trebui să fie foarte atentă la condiţia ei generală după inicident, ideal, sub supraveghere.</p>

<p class="sumar">Sumar:</p>
<ul>
	<li>O persoană electrocutată trebuie să fie deconectată de la sursa de energie electrică. Pentru aceasta, se va localiza şi acţiona cel mai apropiat întrerupător de tensiune. Altfel, dacă este imposibilă localizarea la timp a dispozitivului de înrerupere, victima poate fi îndepărtate de pe circuit prin dezlipire sau lovire cu un obiect din nemetalic, izolator.</li>
	<li>Victimele electrocutărilor necesită asistenţă medicală de urgenţă: verificaţi respiraţia şi pulsul, iar apoi aplicaţi metodele resuscitare cardio-pulmonara pentru menţinerea oxigenării corpului acesteia</li>
	<li>Dacă victima este conştientă după electrocutare, este totuşi necesară supravegherea atentă şi îngrijirea acesteia până la apariţia personalului medical calificat. Există pericolul instalării şocului fiziologic; asiguraţi prin urmare victimei un mediu călduros şi confortabil</li>
	<li>Victimele electrocutărilor pot prezenta probleme cu inima chiar şi după câteva ore de la accident. Pericolul şocului electric nu ia sfârşit după acordarea imediată a asistenţei medicale</li>
</ul>
