<p>Un element de circuit <i>activ</i> este orice tip de component ce poate controla deplasarea electronilor (curentul) pe cale electrică. Pentru ca un circuit să poarte numele de circuit electronic, aceste trebuie să conţină cel puţin un asfel de element activ. Componentele ce nu pot controla curentul prin intermediul unui alt semnal electric, sunt denumite elemente de circuit <i>pasive</i>. Rezistorii, condensatoarele, bobinele, transformatoarele şi chiar şi diodele, toate sunt considerate elemente de circuit pasive. Elementele active includ, printre altele, tuburile cu vid, tranzistoarele, redresoarele cu semiconductoare, şi triacurile.</p>

<p>Toate dispozitivele active controlează curentul prin ele. Unele dispozitive active realizează acest lucru prin intermediul unei tensiuni, iar altele prin intermediul curentulu. Cele care utilizează o tensiune statică ca şi semnal de control, sunt denumite dispozitive <i>controlate în tensiune</i>. Cele care folosesc un alt curent pentru controlul curentului în cauză sunt cunoscute sub numele de dispozitive <i>controlate în curent</i>. Tuburile cu vid sunt dispozitive controlate în tensiune iar tranzistoarele pot fi de ambele tipuri.</p>


