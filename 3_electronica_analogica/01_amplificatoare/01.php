<p>Circuitele electrice reprezintă conexiuni ale conductorilor electrici cu elemente de circuit, în cadrul cărora are loc o deplasare uniformă de electroni. Circuitele electrice adaugă o nouă dimensiune circuitelor electrice, prin faptul că deplasarea electronilor este <i>controlată</i>, într-o oarecare măsură, de un semnal electric adiţional, fie sub formă de curent, fie sub formă de tensiune.</p>

<p>Controlul curentului nu este neapărărat specific electronicii. Întrerupătoarele şi potenţiometrele controlează şi ele deplasarea electronilor. Prin urmare, diferenţa dintre electric şi electronic este dată de modul în care acest control este exercitat în circuit, şi nu neapărat de existenţa sa absenţa acestuia. Întrerupătoarele şi potenţiometrele controlează curentul mecanic, printr-un element acţionat de o anumită forţă fizică externă circuitului. În electronică, pe de altă parte, avea de a face cu elemente speciale, capabile să controleze curentul cu ajutorul unui alt curent, sau prin aplicarea unei tensiuni statice. Cu alte cuvinte, într-un circuit electronic, <i>curentul controlează curentul</i>.</p>

<p>Din punct de vedere istoric, precursorul electronicii moderne a fost inventat de Thomas Edison în 1880 pe când dezvolta becul cu incandescenţă. Edison a descoperit că există un curent electric între filamentul becului şi o placă metalică instalată în interiorul învelişului vidat (figura de jos (b)). Astăzi, acest comportament este cunoscut sub numele de „efectul Edison”. Bateria este necesară doar pentru încălzirea filamentului. Dacă am folosi orice altă modalitate de încălzire a filamentului, efectul ar fi acelaşi.</p>

<p>În 1904, John Flemming a descoperit că introducerea în circuit a unui curent extern (bateria ataşată plăcii, figura de mai sus (b)) se poate realiza doar într-o singură direcţie, de la filament la placă, dar nu şi invers. Această inveţie este cunoscută sub numele de „dioda cu vid”, folosită pentru transformarea (redresarea) curentului alternativ în curent continuu. Adăugarea celui de al treilea electrod de către Lee DeForest (figura de mai sus (c)), a făcut posibil controlul curentului de la filament la placă cu ajutorul unui semnal mai mic. Inveţia <i>tubului cu vid</i> de către DeForest a marcat practic începutul erei electronice.</p>

<p>Tehnologia electronici a cunoscut o revoluţie în anul 1948, odată cu invenţia <i>tranzistorului</i>. Acest component electric minuscul joacă acelaşi rol ca şi un tub cu vid, dar ocupă un loc mult mai mic şi este mult mai ieftin. Tranzistorii realizează controlul curentului cu ajutorul materialelor <i>semiconductoare</i> şi nu prin vid.</p>

