<p>Dispozitivele multijoncţiune sunt o clasă de componente semiconductoare cu histereză, o proprietate prin care un sistem nu se reîntoarce la starea sa iniţială după ce acţiunea perturbatoare este îndepărtată. Un exemplu foarte simplu de histereză îl constituie un întrerupător mecanic: atunci când braţul este apăsat, acesta se va poziţiona în una din cele două poziţii extreme şi va rămâne în acestă poziţie chiar şi după ce forţa exterioară este indepărtată.</p>

<p>Tranzistoarele bipolare cu joncţiune, cele cu efect de câmp şi cele cu efect de câmp cu poartă izolată sunt toate dispozitive fără histereză. Acest lucru înseamnă că ele nu se „prind” într-o anumită stare după ce aplicarea tensiunii sau a curentului exterior încetează. Oricare ar fi semnalul de intrare al dispozitivului într-un anumit moment, acesta va prezenta un răspuns de ieşire previzibil, aşa cum este el definit de curba sa caracteristică. Dispozitivele multijoncţiune, pe de altă parte, sunt dispozitive semiconductoare ce tind să rămână pornite odată ce au fost pornite şi invers, oprite odată ce au fost oprite. O acţiune momentană poate duce la trecerea dispozitivelor dintr-o stare în alte, stare în care vor rămână şi după ce acţiunea externă încetează. Prin urmare, aceste dispozitive sunt folositoare doar ca şi întrerupătoare şi nu pot fi folosite pe post de amplificatoare.</p>

<p>Dispozitivele multijoncţiune sunt construite folosind aceeiaşi tehnologie precum a tranzistoarelor bipolare, şi pot fi de fapt analizate ca şi circuite compuse din perechi de tranzistoare. Cum poate atunci un dispozitiv cu histereză să fie construit din dispozitive ce nu prezintă această proprietate? Răspunsul este de data de reacţia pozitivă. Acestă reacţie tinde să satureze dispozitivul.</p>


