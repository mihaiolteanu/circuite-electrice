<h3>Valenţa</h3>

<p>Electronii din stratul exterior, sau stratul de valenţă, sunt cunoscuţi sub numele de electroni de <i>valenţă</i>. Aceşti electroni sunt responsabil de proprietăţile chimice ale elementelor. Aceştia sunt electronii ce participă la reacţiile chimice cu celelalte elemente. Conform unei reguli chimice simplificate, aplicabilă reacţiilor simple, atomii încearcă să-şi completeze toate locurile libere ale stratului exterior cu electroni. Atomii pot ceda câţiva elecroni pentru a „descoperi” un strat complet, sau pot accepta câţiva electroni pentru a completa ultimul strat (stratul exterior). Ambele procese duc la formarea ionilor. Atomii pot chiar să împartă electroni între ei în încercarea de completare a stratului exterior, ducând la formarea legăturilor moleculare, adică, atomii se asociază pentru formarea unei molecule.</p>

<p>De exemplu, elementele din grupa I din tabelul periodic, Li, Na, K, Cu, Ag şi Au au doar un singur electron de valenţă (numărul de electroni de pe ultimul strat). Toate aceste elemente posedă proprietăţi chimice similare. Aceşti atomi cedează un electron pentru a reacţiona cu alte elemente, iar această proprietatea face ca aceste elemente să fie conductoare excelente de electricitate.</p>

<p><img src="../poze/03394.png" alt="elementele din grupa I; toate au un singur electron de valenţă, electron care este cedat în reacţia cu alte elemente chimice"/></p>

<p>Elementele din grupa VIIA, Fl, Cl şi BR, au toate câte 7 electroni în stratul exterior (stratul de valenţă). Aceste elemente acceptă un electron pentru completarea stratului de valenţă la 8 electroni. În cazul în care aceste elemente acceptă un electron, ele formează ioni negativi. Din moment ce nu cedeazăelectroni, aceste elemente sunt foarte buni izolatori electrici.</p>

<p><img src="../poze/03394.png" alt="elementele din grupa VIIA; toate au şapte electroni de valenţă, prin urmare, acestea acceptă un electron pentru completarea stratului de valenţă"/></p>

<p>De exemplu, un atom de Cl acceptă un electron al unui atom de Na devenind ion negativ Cl<sup>-</sup>, iar atomul de Na devine ion pozitiv, Na<sup>+</sup>. Un <i>ion</i> este un atom, moleculă sau grupare de atomi care are un exces de sarcină electrică pozitivă sau negativă. Acesta este modul în care Na şi Cl se combină pentru formarea NaCl, sarea de masă, care este defapt o pereche de ioni, Na<sup>+</sup>Cl<sup>-</sup>. Fiindcă sarcinile celor doi ioni sunt de semn contrar, cei doi se atrag reciproc.</p>

<p><img src="../poze/03398.png" alt="Atomul de Na doneazăun electron atomului de Cl, formând ioni pozitivi şi negativ de Na, respectiv Cl"/></p>

<p>Structura cristalină a clorurii de sodiu (NaCl) este prezentată în figura de mai jos.</p>

<p><img src="../poze/03400.png" alt="structura cristalină a clorurii de sodiu (NaCl)"/></p>

<p>Elementele din grupa a VIIIA, He, Ne, Ar, Kr şi Xe au toate câte 8 electroni pe stratul de valenţă. Acest lucru înseamnă că aceste elemente nici nu donează dar nici nu acceptă electroni, neparticipând la reacţii chimice cu alte elemente. Toate sunt izolatori electrici şi se găsesc sub formă de gaz la temperatura camerei.</p>

<p><img src="../poze/03396.png" alt="elementele din grupa VIIIA au toate câte 8 electroni pe stratul de valentă, prin urmare, aceste elemente nu cedează şi nici nu acceptă electroni, ceea ce înseamnă ca nu pot participa la reacţii chimice"/></p>

<p>Elementele din grupa IVA, C, Si şi Ge au toate câte 4 electroni în stratul de valenţă. Aceste elemente formează compuşi cu alte elemente, dar nu formează ioni. Acest tip de legătură este cunoscută sub numele de <i>legătură covalentă</i>. Se poate observa că atomul din centru are completat stratul de valenţă prin punerea în comun a electronilor atomilor. Figura de mai jos este o reprezentare bi-dimensională a unui aranjament tri-dimensional. Elementele din această grupă prezintă proprietăţile semiconductoare pe care le vom studia în continuare.</p>

<p><img src="../poze/03397.png" alt="elementele din grupa IVA au toate câte 4 electroni pe stratul de valenţă; aceste elemente sunt studiate în electronică pentru proprietăţile lor semiconductoare"/></p>

<h3>Structura cristalină</h3>

<p>Majoritatea substanţelor inorganice formează o structură ordonată denumită <i>cristal</i> atunci când se formează legături între atomii sau ionii acestora. Chiar şi metalele sunt compuse din cristale, la nivel microscopic. Practic însă, toate metalele industriale au o strucutră policristalină, în afară de materialele semiconductoare ce sunt monocristaline.</p>

<p>Majoritatea metalelor sunt moi şi uşor deformabile pe cale industrială. În timpul prelucrării, microcristalele sunt deformate, iar electronii de valenţă sunt liberi să se deplaseze prin reţeaua cristalină, şi de la cristal la cristal. Electronii de valenţă nu aparţin unui atom anume, ci tuturor atomilor.</p>

<p>Structura cristalină rigidă a NaCl prezentată mai sus, este compusă dintr-o structură regulată repetitivă formată din ioni pozitivi de Na şi ioni negativ de Cl. Odată ce atomii de Na şi Cl formează ionii de Na<sup>+</sup> şi Cl<sup>-</sup> prin transferul unui electron de la Na la Cl, fără existenţa electronilor liberi, electronii nu sunt liberi să se deplaseze prin reţeaua cristalină, o diferenţa mare faţă de metale. Nici ionii nu sunt liberi. Ionii sunt liberi să se deplaseze doar dacă NaCl este dizolvata în apă, dar în acest caz, cristalul nu mai există. Materialele ionice formează structuri cristaline datorită atracţiei electrostatice puternice dintre ionii încărcaţi cu sarcini opuse.</p>

<p>Materialele semiconductoare din grupa IV (C, Si, Ge), formează de asemenea cristale. Fiecare atom formează o legătură chimică covalentă cu alţi patru atomi. Cristalul format este practic o singură moleculă. Structura cristalină este relativ rigidă şi rezistă deformaţiilor. Există un număr relativ mic de electroni liberi prin cirstal.</p>

<p class="sumar">Sumar:</p>
<ul>
	<li>Atomii încearcă să-şi completeze stratul exterior, de valenţă, cu toţi cei 8 electroni (2 electroni pentru stratul inferior). Atomii pot dona, accepta sau împărţi electroni pentru a completa un strat</li>
	<li>Atomii formează adesea structuri ordonate şi rigide denumite cristale</li>
	<li>Un ion pozitiv se formează prin cedarea unui elentron de către un atom neutru</li>
	<li>Un ion negativ se formează prin acceptarea unui elentron de către un atom neutru</li>
	<li>Elementele semiconductoare din grupa IVA, C, Si şi Ge au o structură cristalină de tip diamant. Fiecare atom al cristalului este parte a unei molecule gigantice, formând legături cu alţi patru atomi</li>
	<li>Majoritatea dispozitivelor semiconductoare sunt confecţionate din monocristale</li>
</ul>
