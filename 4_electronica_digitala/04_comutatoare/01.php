<div id="contents">
<p>Un comuntator electric este orice dispozitiv folosit pentru întreruperea deplasării electronilor prin circuit. Comutatoarele sunt practic dispozitive binare: fie sunt complet închise, fie complet deschise. Există o multitudine de tipuri de comutatoare. Vom prezenta câteva din ele mai jos.</p>

<p>Deşi pare ciudat să prezentăm acest subiect elementar aşa de târziu, în capitolele următoare vom explora un domeniu mai vechi al tehnologiei digitale. Aceasta din urmă se bazează pe contacte realizate cu ajutorul comutatoarelor mecanice şi nu pe circuite digitale cu porţi. Prezentarea ambelor metode conduce la o mai bună înţelegere a subiectului de faţă. Acest lucru ne va fi de folos atunci când vom învăţă despre algebra booleană, matematica din spatele circuitelor logice digitale.</p>

<p>Cel mai simplu tip de comutator este acela în care contactul dintre doi conductori electrici se realizează prin acţionarea unui mecanism de deplasare. Există şi comutatoare mult mai complexe, comutatoare conţinând circuite electronice capabile să închidă sau să deschidă circuitul în funcţie de un stimul fizic (precum lumină sau câmp magnetic). Indiferent de caz, rezultatul final al unui comutator este o pereche de terminali ce vor fi conectaţi prin intermediul mecanismului intern al aparatului (comutator inchis), fie vor fi separaţi (comutator deschis).</p>

<p>Orice comutator proiectat să fie folosit de către un operator uman, poartă numele de comutator manual. Există mai multe tipuri de astfel de comutatoare.</p>

<h2>Comutator basculant</h2>

<img class="floatleft" src="../poze/04016.png" alt="comutator basculant; simbol"/>

<p>Aceste tipuri de comutatoare sunt acţionate cu ajutorul unei manete. Această manetă se poate regăsi într-una dintre cele două sau mai multe poziţii disponibile (în funcţie de tip). Comutatorul obişnuit folosit pentru aprinderea şi stingerea luminii în casă, este un bun exemplu de comutator basculant. Majoritatea comutatoarelor basculante se pot regăsi în oricare dintre poziţii. Unele comutatoare sunt însă echipate cu um mecanism intern prin intermediul căruia maneta revine tot timpul într-o poziţie normală, bine stabilită. Funcţionarea (închiderea sau deschiderea circuitului, în funcţie de caz) comutatorului se face doar pentru o perioadă scurtă de timp, după care acesta revina la poziţie iniţială.</p>

<div class="clear"></div>

<h2>Comutator buton</h2>

<img class="floatleft" src="../poze/04017.png" alt="comutator buton; simbol"/>

<p>Comutatoarele buton sunt dispozitive bi-poziţionale acţionate prin intermediul unui buton care apăsat iar apoi eliberat. Majoritatea butoanelor posedă un mecanism intern prin care butonul se reîntoarce la poziţia sa iniţială (ne-apăsat). Prin urmare, acest dispozitiv funcţionează doar pe perioada în care butonul este apăsat, revenind apoi la poziţia sa iniţială. Un bun exemplu de astfel de comutator este butonul de pornire al calculatorului, sau de chemare al liftului. După apăsare, acestea revin la poziţia iniţială.</p>

<div class="clear"></div>

<p>Unele comutatoare pot rămâe în poziţia apăsat până când acesta este tras înapoi. Aceste tipuri de comutatoare sunt prevăzute cu un buton de tip cipercă pentru uşurarea acţiunii.</p>

<h2>Comutator selector</h2>

<img class="floatleft" src="../poze/04018.png" alt="comutator selector; simbol"/>

<p>Comutatoarele selectoare sunt acţionate prin intermediul unui buton rotativ pentru selectarea uneia sau a mai multor poziţii. La fel ca şi comutatoarele basculante, acestea se pot regăsi în oricare dintre poziţii, sau pot conţine mecanise pentru funcţionarea de scurtă durată (revenirea la poziţia normală).</p>

<div class="clear"></div>

<h2>Comutator joystick</h2>

<img class="floatleft" src="../poze/04019.png" alt="comutator joystick; simbol"/>

<p>Un comutator joystick este acţionat prin intermediul unei manete cu un grad de libertate sporit. În funcţie de direcţia de deplasare a manetei, există unul sau mai multe mecanisme de contact ce intră în acţiune. Câteodată, acest lucru depinde şi de distanţa de deplasare a manetei. Cercul şi punctul din simbolul comutatorului indică direcţia de deplasare a manetei pentru acţionarea contactului. Aceste tipuri de comutatoare sunt folosit de obicei pentru macarele şi pentru control industrial.</p>

<h2>Comutatoare de limitare</h2>

<p>Unele comutatoare sunt special concepute pentru acţionarea lor nu de către un operator uman, ci de către mişcarea unui dispozitiv mecanic. Aceste comutatoare de mişcare poartă numele de comutatoare de limitare, datorită faptului că sunt folosite pentru limitarea deplasării unei maşini. Acest lucru se realizează prin întreruperea alimentării unui anumit component, dacă acesta se deplasează prea departe. La fel ca în cazul comutatoarelor manuale, există mai multe tipuri de comutatoare de limitare.</p>

<h2>Comutator de limitare cu manetă</h2>

<img class="floatleft" src="../poze/04021.png" alt="Comutator de limitare cu manetă; simbol"/>

<p>Aceste limitatoare sunt asemănătoare comutatoarelor basculante sau selectoare. În cazul acestora însă, maneta este acţionată de un dispozitiv mecanic, şi nu de către un operator uman.</p>

<div class="clear"></div>

<h2>Comutator de proximitate</h2>

<img class="floatleft" src="../poze/04022.png" alt="Comutator de proximitate; simbol"/>

<p>Comutatoarele de proximitate detectează apropierea unei părţi metalice, fie prin intermediul unui câmp magnetic, fie prin intermediul unui câmp electromagnetic de frecvenţă înaltă. Comtuatoarele de proximitate simple utilizează un magnet permanent pentru acţionarea unui mecanism întrerupător atunci când componenta metalică se apropie prea mult (2-3 cm). Comutatoarele de proximitate mai complexe funcţionează asemenea unui detector de metale, alimentând o bobină cu un curent de frecvenţă înaltă şi măsurând electronic amplitudinea acelui curent. Dacă o componentă metalică (nu neapărat magnetică) se apropie prea mult de bobină, curentul va creşte şi va acţiona mecanismul de monitorizare a circuitului. Simbolul alăturat este al unui comutator de proximitate de tip electronic, indicat prin romb. Simbolul unui dispozitiv non-electric este acelaşi cu simbolul comutatorului de limitare cu manetă.</p>

<div class="clear"></div>

<p>O altă variantă a comutatorului de proximitate o reprezintă comutatorul optic. Acesta este compus dintr-o sursă de lumină si un element fotosensibil. Poziţia elementului mecanic (maşinii) este detectată fie prin întreruperea sau reflexia undei de lumină. Comutatoarele optice sunt folosite în aplicaţii de siguranţă, unde o sursă de lumină poate fi folosită pentru detectarea intrării persoanelor neautorizate într-o zonă periculoasă.</p>

<h2>Comutatoare de proces</h2>

<p>În multe aplicaţii industriale, este necesară o monitorizare a diferitelor mărimit fizice cu ajutorul comutatoarelor. Astfel de dispozitive pot fi folosite pentru pornirea unei alarme, indicând faptul că variabila de proces a depăşit parametrii normali de funcţionare. Sau pot fi folosite pentru oprirea proceselor sau a echipamentelor dacă acele variabile au atins un nivel periculos sau destructiv. Desigur, există mai multe variante de astfel de comutatoare de proces, prezentate mai jos.</p>

<h2>Comutator de viteză</h2>

<img class="floatleft" src="../poze/04024.png" alt="Comutator de viteză; simbol"/>

<p>Aceste comutatoare pot detecta viteza de rotaţie a unui ax prin intermediul unui mecanism montat pe acesta. Desigur, acest lucru este de preferat a se realiza fără un contact fizic între ax şi comutator, caz în care detecţia se realizează optic sau magnetic.</p>

<div class="clear"></div>

<h2>Comutator de presiune</h2>

<img class="floatleft" src="../poze/04025.png" alt="Comutator de presiune; simbol"/>

<p>Presiunea gazului sau a lichidului poate fi utilizată pentru acţionarea unui mecanism de comutare. Această presiune trebuie să fie aplicată unui piston sau unei diafragme, care la rândul ei va converti prsiunea în forţă mecanică.</p>

<div class="clear"></div>

<h2>Comutator de temperatură</h2>

<img class="floatleft" src="../poze/04026.png" alt="Comutator de temperatură; simbol"/>

<p>Un mecanism relativ ieftin de detectare a temperaturii constă dintr-o bandă bimetalică: o bandă subţire formată din două metale diferite poziţionate spate-în-spate. Fiecare metal posedă un coeficient de dilatare termică diferit. Această dilatare termică nu este altceva decât tendinţa corpurilor de a-şi creşte volumul în urma variaţiei temperaturii (de obicei în urma creşterii acesteia, dar există şi excepţii). Fenomenul opus poartă numele de contracţie termică.</p>

<div class="clear"></div>

<p>Atunci când banda se răceşte sau se încălzeşte, coeficienţii de dilatare diferiţi ale celor două metale cauzează curbarea acetei benzi. Curbarea benzii poate fi folosită apoi pentru acţionarea unui mecanism de comutare.</p>

<p>Alte comutatoare de temperatură utilizează un bec de alamă umplut fie cu gaz fie cu lichid, şi un tub ce conectează acest bec de un comutator de presiune. Pe măsură de becul este încălzit, volumul gazului sau al lichidului creşte, generând o creştere de prsiune care mai apoi acţionează mecanismul de comutare.</p>

<h2>Comutator de nivel</h2>

<img class="floatleft" src="../poze/04027.png" alt="Comutator de nivel de lichid; simbol"/>

<p>Un obiect plutitor poate fi folosit pentru acţionarea unui mecanism atunci când nivelul de lichid dintr-un bazin trece peste un anumit nivel. Dacă lichidul este conductor din punct de vedere electric, acesta poate fi folosit ca si conductor pentru închiderea circuitului dintre două sonde metalice. Acestea sunt plasate în bazin la adâncimea corespunzătoare. În majoritatea cazurilor însă, acest lucru nu este practic, ba mai mult, este chiar periculos.</p>

<div class="clear"></div>

<p>Acest tip de comutatoare poate fi folosit şi pentru detectarea nivelului materialelor solide, precum rumeguş, grâu, cărbune sau plante furajere. O metodă des întâlnită pentru această aplicaţie este utilizarea unei mici roţi cu pale metalice sau din lemn, plasată în interiorul recipientului la înălţimea dorită. Aceasta roată este conectată la un motor electric ce o roteşte cu o anumită viteză. Atunci recipientul este umplut cu material solid până la acel nivel, materialul blochează roata şi împiedică rotirea ei. Răspunsul motorului este cel care acţionează mecanismul de comutare.</p>

<p>O altă metodă utilizează un diapazon (instrument format dintr-o bară metalică în formă de U, ce vibrează la lovire). Acesta este introdus în recipient din exterior, la înălţimea dorită. Diapazonul este supus unei vibraţii la frecvenţa de rezonantă prin intermediul unui circuit electronic şi un magnet/electromagnet. Când materialul solid trece de înălţimea la care este montat diapazonul, acesta va atenua vibraţiile diapazonului. Această modificare a amplitudinii vibraţiilor şi/sau frecvenţei este detectată de circuitul electronic.</p>

<h3>Comutator nuclear</h3>

<img class="floatleft" src="../poze/04030.png" alt="Comutator de nivel nuclear; simbol"/>

<p>O ultimă metodă de realizare a unui comutator de nivel pe care o luăm aici în considerare, îl reprezintă comutatorul nuclear. Acesta este compus dintr-un material radioactiv ca şi sursă şi un detector de radiatie. Ambele elemente sunt montate în lungul diametrului recipientului pentru lichid sau pentru material solid.. Dacă înălţimea materialului trece de nivelul mecanismului sursă/detector, acesta va atenua puterea recepţionată de detectorul de radiaţie. Această descreştere a radiaţiei pe detector poate fi folosită pentru acţionarea unui mecanism  de comutare, fie pentru măsurarea nivelului, fie pentru declanşarea unei alarme sau chiar şi pentru controlul nivelului din recipient.</p>

<div class="clear"></div>

<p>Atât sursa cât şi detectorul sunt montate în exteriorul recipientului, singurele elemente ce pătrung în interior sunt radiaţiile. Sursele de radiaţie sunt extrem de slabe şi nu prezintă niciun risc imediat operatorilor sau personalului de întreţinere.</p>

<h2>Comutator de curgere</h2>

<img class="floatleft" src="../poze/04028.png" alt="Comutator de curgere; simbol"/>

<p>Introdus într-o conductă, un comutator de curgere va detecta viteza de curgere a unui gaz sau a unui lichid. În momentul în care această viteză depăşeşte o anumită limită, se va acţiona mecanismul de comutare. De obicei se folosesc pale sau aripi ce sunt împinse de curgerea substanţei respective. O metodă alternativă constă în detectarea căderii de presiune pe o anumită porţiune a conductei.</p>

<div class="clear"></div>

<h2>Observaţie</h2>

<p>Desigur, există tot timpul mai multe metode de implentare a unui comutator pentru monitorizarea sau controlul unui procez ficiz. De obicei nu există un singur comutator „perfect” pentru nicio aplicaţie, deşi unele prezintă câteva avantaje clare faţă de altele. Comutatoarele trebuie alese inteligent în funcţie de aplicaţia în cauză. Acest lucru va determina funcţionarea lor eficientă şi sigură.</p>

<p class="sumar">Sumar:</p>
<ul>
	<li>Un comutator este un dispozitiv electric, de obicei electromecanic, folosit pentru a controla continuitatea electrică dintre două puncte distincte</li>
	<li>Comutatoarele manuale sunt acţionate de operatori umani</li>
	<li>Comutatoarele de limitare sunt acţionate de poziţia elementelor mecanice</li>
	<li>Comutatoarele de proces sunt acţionate de variaţia unei anumite variabile fizice de proces, precum temperatură, presiune, nivel, etc.</li>
</ul>
