<p>Circuitele digitale cu porţi logice sunt confecţionate ca şi circuite integrate: toţi tranzistori şi rezistorii ce intră în componenţa circuitului sunt construiţi pe o singură bucată de material semiconductor. Prin urmare, dacă avem nevoie de un număr relativ de porţi logice, putem folosi circuite integrate sub forma capsulelor DIP. Aceste circuite integrate sunt disponibile cu un număr par de pini, cele mai comune fiind cu 8, 14, 16, 18 sau 24 de pini</p>

<p>Numărul de catalog al acestor capsule indică numărul şi tipul porţilor conţinute în pachet. Aceste numere de catalog sunt standardizate, ceea ce înseamnă că un circuit „74LS02” produs de Motorola este indentic ca şi funcţionalitate cu un circuit „74LS02” produs de Fairchild sau de oricare alt producător. Codul de litere ce precedă aceste numere de catalog sunt însă unice fiecărui producător în parte. De exemplu „SN74LS02” reprezintă o capsulă cu patru porţi logice SAU-negat, produsă de Motorola. Un „DM74LS02” este acelaşi circuit din punct de vedere funcţional, dar produs de Fairchild.</p>

